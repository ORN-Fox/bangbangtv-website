<script lang="ts">
  import { Options, Vue } from 'vue-class-component';
  import $ from 'jquery';
  import 'owl.carousel';
  import 'owl.carousel/dist/assets/owl.carousel.css';
  import Masonry from 'masonry-layout';

  import HelloWorld from '@/components/HelloWorld.vue'; // @ is an alias to /src

  @Options({
    components: {
      HelloWorld,
    },
  })
  export default class HomeView extends Vue {
        mounted() {
            /*------------------
                Background Set
            --------------------*/
            $('.set-bg').each(function (_index, element) {
                let bg = $(element).data('setbg');
                $(element).css('background-image', 'url(' + bg + ')');
            });

            /*------------------
                Hero Slider
            --------------------*/
            ($('.hero__slider') as any).owlCarousel({
                loop: true,
                dots: true,
                mouseDrag: false,
                animateOut: 'fadeOut',
                animateIn: 'fadeIn',
                items: 1,
                margin: 0,
                smartSpeed: 1200,
                autoHeight: false,
                autoplay: true,
            });

            let dot = $('.hero__slider .owl-dot');
            dot.each(function (_index, element) {
                let index: any = $(element).index() + 1;
                if (index < 10) {
                    $(element).html('0').append(index);
                } else {
                    $(element).html(index);
                }
            });

            // Masonary
            new Masonry('.work__gallery', {
                itemSelector: '.work__item',
                columnWidth: '.grid-sizer',
                gutter: 10
            });
        }
  }
</script>